      AND conrelid = 'public.universal_fields'::regclass
  ) THEN
    ALTER TABLE public.universal_fields
      ADD CONSTRAINT universal_fields_field_order_check
      CHECK (field_order >= 0);
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
